      <p>5 다른 컴퓨터 코드, 파일, 프로그램을 포함하고 있는 자료를 게시, 게재, 전자우편으로 보내는 행위 등 다른 사용자의 개인정보를 수집 또는 저장하는 행위</p>

      <p>6 재물을 걸고 도박하거나 사행행위를 하는 행위</p>

      <p>7 윤락행위를 알선하거나 음행을 매개하는 내용의 정보를 유통시키는 행위</p>

      <p>8 기타 불법적이거나 부당한 행위</p>

      <p>4. 회원은 이 약관 및 관계법령에서 규정한 사항을 준수하여야 합니다.</p>

      <p>제 17 조 개인정보보호정책</p>

      <p>1. 회사는 이용 신청 시 회원이 제공하는 정보를 통하여 회원에 관한 정보를 수집하며, 회원의 개인정보는 본 이용계약의 이행과 본 이용계약상의 서비스제공을 위한 목적으로 이용합니다.</p>

      <p>2. 회사는 서비스 제공과 관련하여 취득한 회원의 정보를 본인의 승낙 없이 제 3자에게 누설 또는 배포할 수 없으며 상업적 목적으로 사용할 수 없습니다. 다만, 다음의 각 호의 경우에는 그러하지 아니합니다.</p>

      <p>1 관계 법령에 의하여 수사상의 목적으로 관계기관으로부터 요구가 있는 경우</p>

      <p>2 정보통신윤리위원회의 요청이 있는 경우</p>

      <p>3 기타 관계법령에서 정한 절차에 따른 요청이 있는 경우</p>

      <p>제 18 조 회원에 대한 통지</p>

      <p>1. 회사가 회원에 대한 통지를 하는 경우, 회원이 등록한 전자우편주소로 통지합니다.</p>

      <p>2. 회사는 불특정 다수 회원에 대한 통지의 경우, 1주일 동안 사이트에 게시함으로써 개별 통지에 갈음할 수 있습니다. 다만, 회원 본인의 거래와 관련하여 중대한 영향을 미치는 사항에 대하여는 개별 통지합니다.</p>

      <p>제 19 조 개별 서비스에 대한 약관 및 이용조건</p>

      <p>회사는 제공하는 서비스의 구체적 내용에 따라 개별 서비스에 대한 약관 및 이용조건을 각 개별서비스마다 별도로 정하여 회원의 동의를 얻을 수 있습니다. 이 경우 개별 서비스에 대한 이용약관 등이 본 약관에 우선합니다.</p>

      <p>제 20 조 디자인의 귀속</p>

      <p>1. 제출된 디자인에 대한 저작권은 아티스트에게 있습니다.</p>

      <p>2. 아티스트는 디자인을 사이트에 제출하는 행위가 회사가 해당 디자인이 적용된 제품을 판매하는 것에 동의하는 것임을 인정합니다.</p>

      <p>제 21 조 아티스트의 의무</p>

      <p>1. 본 약관은 제 3자의 어떠한 법적 합의와도 상충되지 않는 것을 기본 전제로 합니다. 아티스트가 제출한 디자인이 이미 제 3자와 법적으로 합의된 바가 있다면 즉시 회사에게 알려야 합니다.</p>

      <p>2. 아티스트는 전항의 디자인에 대한 권리에 대해서 회사와 양도 또는 사용권 허여 계약을 한 경우, 그와 관련된 손해에 대하여 배상책임을 져야 합니다.</p>

      <p>3. 사이트에서 펀딩을 개설하는 행위는 아티스트가 해당 펀딩을 개설하기 위해 필요한 상표, 저작권, 지식재산권 및 소유권을 보유하고 있다는 것을 전제로 합니다. 이에는 펀딩을 개설하는데 사용된 모든 이름, 이미지, 디자인, 텍스트 등이 포함됩니다. 아티스트는 회사가 요청 시에 자신의 펀딩에 대한 권리를 입증할 책임이 있습니다.</p>

      <p>4. 아티스트는 본인 컨텐츠의 원본을 보관할 책임이 있습니다. 회사는 아티스트가 펀딩에 대한 컨텐츠 및 정보를 삭제하거나 저장에 실패한 것에 대해 책임을 지지 않습니다.</p>

      <p>제 22 조 펀딩 페이지 생성과 수정 및 취소</p>

      <p>1. 아티스트는 회사가 정한 약식과 절차에 따라 펀딩을 생성하고, 사이트에 펀딩을 게시 또는 등록하여 다른 회원으로부터 펀딩하기를 받을 수 있습니다.</p>

      <p>2. 펀딩 등록 시기는 아티스트가 이 약관과 펀딩 약관에 동의하고, 사이트에 게시한 시점으로 합니다.</p>

      <p>3. 회사는 아티스트가 게시한 펀딩에 대해 다음과 같은 내용을 검토한 후, 펀딩 승인 여부에 대한 연락을 취할 수 있습니다.<br />
        1 회사의 펀딩 페이지 생성 규정과 절차를 준수하였는가</p>

        <p>2 회사가 제공한 펀딩 생성 항목을 빠짐없이 기재하였는가</p>

        <p>3 회사가 요구한 펀딩 관련 파일 (이미지 등)을 규격에 맞게 제공하였는가</p>

        <p>4 관계법령에 위배되거나 사회의 안녕질서 혹은 미풍양속을 저해하려는 목적이 있지는 않은가</p>

        <p>5 범죄적 행위, 국익 또는 사회적 공익을 저해하려는 목적이 있지는 않은가</p>

        <p>6 타인의 명예를 손상시키거나 불이익을 주려는 목적이 있지는 않는가</p>

        <p>7 기타 회사가 정한 펀딩 생성 조건에 부합하는가</p>

        <p>4. 아티스트가 생성을 신청한 펀딩 내용에 허위, 기재누락, 오기, 저작권법 위배 등 관계 법령 위배가 있어서는 안되며, 아티스트가 이를 준수하지 않아 발생한 불이익에 대해서는 아티스트가 모든 책임을 부담해야 합니다.</p>

        <p>5. 아티스트가 작성 및 제공한 펀딩 내용 및 파일에 대한 모든 권리 및 책임은 해당 아티스트에게 있으며, 회사는 아티스트가 등록한 펀딩의 내용이 다음 각 항에 해당한다고 판단되는 경우 사전통지 없이 삭제할 수 있고 이에 대하여 회사는 어떠한 책임도 지지 않습니다.</p>

        <p>1 아티스트가 제공한 회원정보 또는 펀딩 정보가 허위임이 판명된 경우</p>

        <p>2 제 3자의 재산권과 기타 권리를 침해하는 내용인 경우</p>

        <p>3 회사로부터 사전승인 받지 아니한 내용을 게시한 경우</p>

        <p>4 아티스트가 회사 사이트와 게시판에 음란물을 게재하거나 음란사이트를 링크하는 경우</p>

        <p>5 아티스트가 펀딩 정보를 불법적으로 변경 또는 조작한 경우<br />
          6 사전에 공지한 펀딩 관련 활동을 미실시한 경우</p>

          <p>7 기타 관계법령에 위반된다고 판단되는 경우</p>

          <p>6. 위 4항의 이유로 발생한 분쟁과 관련하여, 아티스트는 회사와 회사의 자회사, 관계사 그리고 에이전시 및 그 임원 및 직원에게 책임 및 손해 배상을 요구할 수 없습니다.</p>

          <p>7. 승인되어 등록된 펀딩의 경우, 내용을 더 이상 변경할 수 없습니다. 내용을 변경하고 싶은 경우, 등록된 본인의 펀딩을 삭제하고 내용을 수정하여 다시 펀딩을 개설할 수 있습니다.</p>

          <p>8. 승인되어 등록된 펀딩에 큐레이터(일반 회원)이 발생한 경우, 내용을 변경하거나 삭제하실 수 없습니다. 변경이 금지된 사항을 아티스트가 임의로 수정, 변경하거나 삭제한 경우, 큐레이터(일반 회원)이 손해배상 청구 등 법적 대응을 할 수 있으며, 이로 인한 불이익에 대하여 회사는 책임을 지지 않습니다.</p>

          <p>9. 아티스트는 펀딩이 승인되어 등록된 때부터 펀딩 모집 기한이 마감될 때까지, 승인되어 등록된 펀딩에 제공된 디자인을 회사가 영위하는 사업과 동일한 사업을 영위하는 다른 업체 등에 상업적으로 제공할 수 없습니다.</p>

          <p>10. 공개 승인된 펀딩은 모집 기한이 마감된 후에도 사이트 혹은 서비스 관련 웹사이트에 게시될 수 있습니다.</p>

          <p>제 23 조 펀딩 목표 금액 달성과 기금액 전달</p>

          <p>1. 펀딩의 기금 모집 결과는 아티스트가 지정한 펀딩 모집 기한이 마감된 시점에 펀딩하기된 총 인원을 기준으로 아티스트가 지정한 최소 인원 이상이면 성사, 미만이면 무산으로 판단합니다.</p>

          <p>2. 펀딩의 최종 모집 인원에는 모집 기한 마감 전의 취소 및 변경신청이 반영될 수 있습니다.</p>

          <p>3. 펀딩의 인원 모집이 무산될 시 그 동안 모집된 금액에 대해서 해당 펀딩을 개설한</p>

          <p>아티스트는 어떠한 권리도 주장할 수 없으며, 회사는 사전에 고지한 절차에 따라 큐레이터(일반 회원)들로부터 예정된 기금액의 이체를 취소합니다.</p>

          <p>4. 성공한 펀딩의 아티스트는 판매가에서 회사가 부담하는 제작 및 운영 비용과 프리랜서 수입에 대한 세금이 제해진 수익을 얻게 됩니다. 운영 비용은 모집 인원에 따라 상이하며, 사이트는 본 내용을 아티스트가 알기 쉽게 제공해야 합니다.</p>

          <p>5. 회사는 인원 모집이 성사된 펀딩에 한해 모집 마감 후 최대 14일 이내 아티스트에게 최종 기금액과 아티스트에게 지급될 수익 내역을 고지합니다. 아티스트는 이 고지일로부터 14일 이내에 전화, 전자우편, 이메일, 사이트 1:1문의를 통해 문의사항과 이의사항을 회사에 알려야 하며, 이를 이행하지 않아 발생하는 불이익에 대하여 회사는 책임을 지지 않습니다.</p>

          <p>6. 회사는 펀딩이 목표 인원을 달성해서 마감한 이후에 결제 요청을 하고 결제 성공한 기금액 중 회사가 부담하는 제작 및 운영 비용과 프리랜서 수입에 대한 세금이 제해진 수익을 아티스트가 지정한 은행계좌로 지급합니다. 회사는 펀딩 마감일로부터 최대 1개월 안으로 모집된 기금액 중 아티스트의 수익분을 아티스트가 지정한 은행계좌로 최종 지급합니다.</p>

          <p>7. 모집된 기금액 중 아티스트의 수익분에 대한 지급과 관련하여 회사는 아티스트의 신분증 사본, 통장사본 등 신분을 확인할 수 있는 자료를 요청할 수 있으며, 아티스트 본인 명의의 은행계좌임이 확인되었을 경우에만 지정한 계좌로 아티스트의 수익분을 송금합니다.</p>

          <p>8. 아티스트는 사이트에 명시된 펀딩 절차를 준수해야 하며, 사이트는 미 준수시 펀딩이 종료하거나 아티스트에 수익금 전달을 취소할 수 있습니다. 사이트는 펀딩 절차를 아티스트가 알기 쉽게 제공해야 합니다.</p>

          <p>제 24 조 펀딩 펀딩하기 신청</p>

          <p>1. 펀딩 펀딩하기는 회원만 가능합니다.</p>

          <p>2. 회원은 사이트 상에서 다음 또는 이와 유사한 방법에 의하여 펀딩하기를 신청하며, 회사는 회원이 펀딩하기 신청을 함에 있어서 다음의 각 내용을 알기 쉽게 제공하여야 합니다.</p>

          <p>① 재화 등의 검색 및 선택</p>

          <p>2 받는 사람의 성명, 주소, 전화번호, 전자우편주소(또는 이동전화번호), 계좌 정보 등의 입력</p>

          <p>3 배송료 등의 비용부담과 관련한 내용에 대한 확인</p>

          <p>4 결제방법의 선택</p>

          <p>3. 회사가 제3자에게 구매자 개인정보를 제공·위탁할 필요가 있는 경우 실제 구매신청 시 구매자의 동의를 받아야 하며, 회원가입 시 미리 포괄적으로 동의를 받지 않습니다. 이 때 회사는 제공되는 개인정보 항목, 제공받는 자, 제공받는 자의 개인정보 이용 목적 및 보유, 이용 기간 등을 구매자에게 명시하여야 합니다. 다만 「정보통신망이용촉진 및 정보보호 등에 관한 법률」 제25조 제1항에 의한 개인정보 취급위탁의 경우 등 관련 법령에 달리 정함이 있는 경우에는 그에 따릅니다.</p>

          <p>제 25 조 펀딩 펀딩하기 결제</p>

          <p>1. 펀딩을 펀딩하기하고자 하는 회원은 이 중 회사가 제공하는 결제수단을 통하여 결제 예약할 수 있습니다.</p>

          <p>① 선불카드, 직불카드, 신용카드 등의 각종 카드 결제</p>

          <p>② 무통장 입금</p>

          <p>2. 펀딩 결제는 펀딩 참여자의 수가 목표 인원 이상이 되어 펀딩이 성공한 이후, 최대 3일 이내에 이루어집니다. 단, 카드사 등 금융기관의 사정에 따라 결제 완료되는 기간이 지연될 수 있습니다.</p>

          <p>제 26 조 펀딩 제품의 공급</p>

          <p>1. 펀딩이 최소 목표 금액을 달성하여 펀딩이 성공한 경우, 회사는 즉시 펀딩하기 참가 수에 맞춰 제품 제작 공정에 들어갑니다. 제품 제작은 최대 14일 이내에 이루어지며 제품 제작 완료 이후 포장 등 기타의 필요한 조치를 취하고 최대 5일 이내에 큐레이터(일반 회원)들에게 발송됩니다.</p>

          <p>2. 회사는 이용자가 구매한 재화에 대해 배송수단, 수단별 배송비용 부담자, 수단별 배송기간 등을 명시합니다. 만약 회사가 약정 배송기간을 초과한 경우에는 그로 인한 이용자의 손해를 배상하여야 합니다. 다만 회사가 고의, 과실이 없음을 입증한 경우에는 그러하지 아니합니다.</p>

          <p>제 27 조 펀딩 제품 구매 취소 및 환불</p>

          <p>1. 회사는 큐레이터(일반 회원)이 후원한 펀딩이 회사에서 정한 최소 목표 기금액을 충족시키지 못해 펀딩을 착수 또는 완료할 수 없을 때에는 사유발생일로부터 최대 5일 이내(영업일 기준)에 예정된 기금액 이체에 대해 취소 처리를 시행합니다. 단, 실제로 결제취소에 따른 환불 처리는 카드사 등 금융기관에 따라 완료되는 기간이 달라질 수 있습니다.</p>

          <p>2. 펀딩이 마감되는 즉시 펀딩하기된 제품들에 대한 생산공정에 들어가기 때문에, 펀딩하기 취소는 전자상거래 등에서의 소비자 보호에 관한 법률 및 동법 시행령에 의거하여 펀딩의 모집 기한 이내에만 가능하며, 모집 기한이 지나면 펀딩하기를 취소할 수 없습니다.</p>

          <p>3. 펀딩 모집기간이 마감된 이후, 펀딩하기에 대한 대가로 받게 되는 펀딩 제품은 이용자의 주문에 따라 개별적으로 생산되는 재화이기 때문에 대한 환불은 불가합니다.</p>

          <p>4. 이용자는 재화 등을 배송 받은 경우 다음 각 호의 1에 해당하는 경우에는 반품 및 교환을 할 수 없습니다.</p>

          <p>① 이용자에게 책임 있는 사유로 재화 등이 멸실 또는 훼손된 경우(다만, 재화 등의 내용을 확인하기 위하여 포장 등을 훼손한 경우에는 청약철회를 할 수 있습니다)</p>

          <p>② 이용자의 사용 또는 일부 소비에 의하여 재화 등의 가치가 현저히 감소한 경우</p>

          <p>③ 시간의 경과에 의하여 재판매가 곤란할 정도로 재화 등의 가치가 현저히 감소한 경우</p>

          <p>④ 같은 성능을 지닌 재화 등으로 복제가 가능한 경우 그 원본인 재화 등의 포장을 훼손한 경우</p>

          <p>⑤ 청약철회를 인정하는 경우 회사에게 회복할 수 없는 중대한 피해가 예상되어 사전에 청약철회 제한에 대하여 고지하고 이용자의 동의를 받은 경우</p>

          <p>(예. 펀딩으로 펀딩하기된 제품 등 이용자의 주문에 따라 개별적으로 생산되는 재화로 인정되는 경우)</p>

          <p>5. 공급받은 재화 등의 반환에 필요한 비용은 이용자가 부담합니다. 회사는 이용자에게 청약철회 등을 이유로 위약금 또는 손해배상을 청구하지 않습니다. 다만 재화 등의 내용이 표시·광고 내용과 다르거나 계약내용과 다르게 이행되어 청약철회 등을 하는 경우 재화 등의 반환에 필요한 비용은 회사가 부담합니다.</p>

          <p>6. 이용자가 재화 등을 제공받을 때 발송비를 부담한 경우에 회사는 청약철회 시 그 비용을 누가 부담하는지를 이용자가 알기 쉽도록 명확하게 표시합니다.</p>

          <p>7. 개별 서비스의 성격에 따라 회사는 별도 약관 및 이용조건에 따른 취소 및 환불 규정을 정할 수 있으며, 이 경우 개별 약관 및 이용조건 상의 취소 및 환불규정이 우선 적용됩니다.</p>

          <p>기타 본 약관 및 사이트의 이용안내에 규정되지 않은 취소 및 환불에 대한 사항에 대해서는 소비자 피해보상규정에서 정한 바에 따릅니다.</p>

