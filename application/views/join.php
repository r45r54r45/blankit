          <p>이용자는 회사가 정한 방법(신용카드, 실시간 계좌이체, 가상계좌, 포인트 등)을 통해 사이트 내에서 결제가 가능하며, 정확하고 안전한 결제 처리를 위해 관계법령에 따라 소비자 정보를 결제 대행사에 제공합니다.</p>

