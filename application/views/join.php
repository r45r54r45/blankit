          <p>3. 서비스 수수료 정책은 회사의 사정에 따라 변경될 수 있으며, 회사는 사전에 사이트를 통해 변경내용을 고지합니다.</p>

          <p>4. 회원은 회사의 서비스 수수료 외에 결제수단 (신용카드, 핸드폰, 계좌이체 등)이용에 따른 수수료를 부담해야 합니다.</p>

          <p>5. 회사의 서비스 이용에 따라 회원에게 부과되는 혹은 의무가 있는 모든 세금을 성실하게 신고하고 납부할 의무와 책임이 있습니다.</p>

          <p>제 37 조 마일리지</p>

          <p>1. 회사는 회원이 상품을 구매하거나, 상품평의 작성 등의 경우 회원에게 일정한 마일리지를 부여할 수 있습니다. 다만, 상품구매 후 취소 또는 반품을 할 경우에는 상품 구매 시 회사가 부여한 마일리지를 회수합니다. 그 구체적인 운영방법은 회사의 운영정책에 의합니다.</p>

          <p>2. 마일리지는 상품 구매시 사용가능 기준 하에 현금가액과 동일하게 사용할 수 있으나 (단 사용불가 사전고지품목 제외), 현금으로 환불되지는 않습니다. 또한, 마일리지 서비스는 회원에게만 제공되며 타인에게 양도할 수 없습니다.</p>

          <p>3. 부여된 마일리지는 부여일로부터 5년간 유효합니다. 다만, 회사가 별도로 유효기간을 정한 때에는 이에 따릅니다. 유효기간이 경과한 마일리지는 자동적으로 소멸하게 됩니다.</p>

          <p>4. 회사의 회원 중에 1년간 거래가 없고, 적립된 마일리지가 5,000포인트 미만인 경우에는 휴면회원으로 간주하여 회사는 해당 회원의 적립된 마일리지를 회수하게 됩니다.</p>

          <p>5. 회원을 탈퇴한 경우 마일리지는 소멸됩니다.</p>

