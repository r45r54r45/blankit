          <p>① 인터넷뱅킹 등의 각종 계좌이체</p>

          <p>2 선불카드, 직불카드, 신용카드 등의 각종 카드 결제</p>

          <p>3 온라인무통장입금</p>

          <p>4 마일리지 등 회사가 지급한 포인트에 의한 결제</p>

