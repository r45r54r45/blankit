          <p>1. 회사와 아티스트는 펀딩이 마감된 이후에, 회사가 원하는 경우 아티스트와 협의하여 사이트 내 스토어와 오프라인 매장들에서 제품을 판매하는 별도의 계약을 체결할 수 있습니다.</p>

          <p>2. 스토어와 오프라인 매장에서 제품을 판매하는 경우의 아티스트에 대한 수익 배분 정책은 펀딩 펀딩하기 결과에 따라 아티스트에게 제공되는 수익 정책과 다르며, 이 경우의 수익 배분 방식과 구체적인 절차는 별도 계약서에 구체적으로 제시됩니다.</p>

          <p>제 30 조 스토어 구매 신청</p>

          <p>1. 이용자는 사이트 상에서 다음 또는 이와 유사한 방법에 의하여 구매를 신청하며, 회사는 이용자가 구매신청을 함에 있어서 다음의 각 내용을 알기 쉽게 제공하여야 합니다.</p>

